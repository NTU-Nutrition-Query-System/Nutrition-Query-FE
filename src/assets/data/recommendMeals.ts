export const getMealDetail = (calorie: number) => {
    const mealDetails = recommendMeals.map((recommendMeal:any) => {
        const meals = recommendMeal.meals.map((meal:any) => {
            const mealDetail = meal.map((foodId:any) => {
                const food = allFoods.find((food:any) => food.id === foodId);
                return food;
            });
            return mealDetail;
        });
        return {
            calories: recommendMeal.calories,
            meals,
        };
    });

    // 找到最接近的卡路里值
    const closestMeal = mealDetails.reduce((prev, curr) => 
      Math.abs(curr.calories - calorie) < Math.abs(prev.calories - calorie) ? curr : prev
  );

  return closestMeal.meals;
}


